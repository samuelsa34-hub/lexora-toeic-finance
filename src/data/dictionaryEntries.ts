export function getDictionaryEntry(word: string): DictionaryEntry | undefined {
  return dictionaryEntries.find(e =>
    e.word.toLowerCase() === word.toLowerCase() || e.id === word.toLowerCase()
  )
}

export function searchDictionary(query: string): DictionaryEntry[] {
  const q = query.toLowerCase().trim()
  if (!q) return []
  return dictionaryEntries.filter(e =>
    e.word.toLowerCase().includes(q) ||
    e.definition.toLowerCase().includes(q) ||
    (e.tags || []).some(t => t.includes(q)) ||
    (e.synonyms || []).some(s => s.toLowerCase().includes(q)) ||
    (e.confusedWith || []).some(c => c.toLowerCase().includes(q))
  )
}

export function getDictionaryEntriesForCategory(category: string): DictionaryEntry[] {
  return dictionaryEntries.filter(e => e.linkedCategory === category || (e.tags || []).includes(category))
}

export function getDictionaryEntriesForTags(tags: string[]): DictionaryEntry[] {
  return dictionaryEntries.filter(e => tags.some(t => (e.tags || []).includes(t)))
}
